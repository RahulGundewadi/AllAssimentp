package Assiment1;

public class QB2 {
//2. Declare and initialize all 8 primitive datatypes, store them in their wrapper class and print those as a Object.
	public static void main(String[] args) {
		int a = 10;
		Integer wA = a;
		System.out.println(wA);
		
		byte b = 110;
		Byte wB = b;
		System.out.println(wB);
		
		char c = 'v';
		Character wC = c;
		System.out.println(c);
		
		boolean m = false;
		Boolean wM = m;
		System.out.println(wM);
		
		short s = 126;
		Short wS = s;
		System.out.println(wS);
		
		long l = 151626295l;
		Long wL = l;
		System.out.println(wL);
		
		float f = 595.21f;
		Float wF = f;
		System.out.println(wF);
		
		double d = 62.26265f;
		Double wD = d;
		System.out.println(wD);
	}
}
